#include <unistd.h>

int	main(int argv, char *argc[])
{
	int	i;
	int	j;

	if (argv > 1)
	{
		i = argv - 1;
		while (i >= 1)
		{
			j = 0;
			while (argc[i][j])
			{
				write(1, &argc[i][j], 1);
				j++;
			}
			write(1, "\n", 1);
			i--;
		}
	}
}

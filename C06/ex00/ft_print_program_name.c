#include <unistd.h>

int	main(int argv, char *argc[])
{
	int	i;

	i = 0;
	if (argv >= 1)
	{
		while (argc[0][i])
		{
			write(1, &argc[0][i], 1);
			i++;
		}
		write(1, "\n", 1);
	}
}

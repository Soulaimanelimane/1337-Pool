int	ft_is_prime(int nb)
{
	int	i;

	i = 2;
	if (nb <= 1)
		return (0);
	while (i < nb)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}

int	ft_find_next_prime(int nb)
{
	int	i;

	if (ft_is_prime(nb))
		return (nb);
	else
	{
		i = nb + 1;
		while (ft_is_prime(i) != 1)
			i++;
		return (i);
	}
}
